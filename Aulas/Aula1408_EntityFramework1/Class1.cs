           É um framework objeto-relacional que permite que o .NET consiga 
           trabalhar com o banco de dados relacional usando seus objetos. 
           Eliminando o tempo que os programadores perdiam montando as querys.

           Algumas de suas principais funcionalidades são que ele é multiplataforma, ou seja, 
